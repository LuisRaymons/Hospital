    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        
        if(this.txtuser.getText().equals("") && this.txtpassword.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Se require un usuario y una contraseña");
        } else{
            String usuario = this.txtuser.getText();
            String Password = this.txtpassword.getText();
                
            UsuarioModel um = this.uc.Buscarusuarios(usuario, Password);
            // validar el correo
            if(um.getUsuario() != null){
                PrincipalView pv = new PrincipalView(um.getNombre(),um.getTipo());
                this.dispose();
                pv.setVisible(true);
            } else{
                JOptionPane.showMessageDialog(null, "No se encontro el usuario");
            }
        }
    }//GEN-LAST:event_btnloginActionPerformed

